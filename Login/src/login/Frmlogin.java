    public void sistema(){
        
        String usuario=txtusuario.getText();
        String clave=String.valueOf(txtcontra.getPassword());
        
        if((usuario.equals("Edwar")) && (clave.compareTo("123456")==0)){
            JOptionPane.showMessageDialog(null, "Bienvenido al sistema" + " " + usuario);
            
            sistema form=new sistema();
            form.setVisible(true);
            this.dispose();
        }
        else{
            JOptionPane.showMessageDialog(null, "Error, vuelva a ingresar sus datos nuevamente");
            txtcontra.setText("");
            txtusuario.setText(null);
        }
        
        
    }
    
    

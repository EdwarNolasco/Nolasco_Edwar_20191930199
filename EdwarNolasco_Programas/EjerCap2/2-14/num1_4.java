//Aplicación que muesta los números del 1 al 4 en la misma línea, con cada par de números adyacentes separado por un espacio.
//Mediante instrucción: System.out.println.
public class num1_4{
    public static void main(String[] args){
        System.out.println("12 34");

        System.out.print("1");
        System.out.print("2");
        System.out.print(" ");
        System.out.print("3");
        System.out.print("4");

        System.out.printf("\n%d%d %d%d",1,2,3,4);
    }
}